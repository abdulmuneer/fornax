        "| Item | Current generated evidence status | G1 closure rule |",
        "|---|---|---|",
        "| v0 target contract | draft/validation tooling exists; final TL/SP sign-off still open | memory headroom and predicted throughput bar signed off |",
        "| Concurrency-market evidence | contract fields exist; persona evidence still open | persona supplies saturation concurrency or scope narrows |",

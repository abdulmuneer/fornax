def _warning_table(warnings: list[str]) -> list[str]:
    rows = ["| Category | Message |", "|---|---|"]
    if not warnings:
        rows.append("| ok | no generator warnings |")
        return rows
    rows.extend(f"| warning | {_escape_table(warning)} |" for warning in warnings)
    return rows


def _milestone_table(kickoff: date | None) -> list[str]:
    rows = ["| Milestone | Phase | Window | Date range | Gate |", "|---|---:|---|---|---|"]
    for name, phase, window, gate in _MILESTONES:
        rows.append(
            f"| {name} | {phase} | {window} | {_date_range(kickoff, window)} | {gate} |"
        )
    return rows


def _staffing_table(ker_status: str) -> list[str]:
    rows = ["| Role | Phase-0 load | Required answer | Status |", "|---|---|---|---|"]
    for role, load, answer in _ROLES:
        status = ker_status if role == "KER" else "assign named owner before G1"
        rows.append(f"| {role} | {load} | {answer} | {status} |")
    return rows


def _date_range(kickoff: date | None, window: str) -> str:
    if kickoff is None:
        return "unset"
    if window == "W0":
        return kickoff.isoformat()
    if window.endswith("+"):
        start = _week_number(window[:-1])
        return f"{_week_date(kickoff, start)}+"
    if "-" in window:
        start_text, end_text = window.split("-", 1)
        start = _week_number(start_text)
        end = _week_number(end_text)
        return f"{_week_date(kickoff, start)} to {_week_date(kickoff, end)}"
    return "unset"


def _week_number(value: str) -> int:
    if not value.startswith("W"):
        raise ValueError(f"invalid week marker: {value}")
    return int(value[1:])


def _week_date(kickoff: date, week: int) -> str:
    return (kickoff + timedelta(weeks=week)).isoformat()


def _escape_table(value: Any) -> str:
    return str(value).replace("|", "\\|")

from __future__ import annotations

from datetime import date, datetime, timedelta
from typing import Any


KER_STATUS_VALUES = (
    "unassigned",
    "in-team",
    "hireable",
    "contractable",
    "unavailable",
)
SCOPE_VALUES = ("pending", "full", "narrowed")

_MILESTONES = [
    ("M0 Architecture baseline", "-", "W0", "G0 passed"),
    ("M1 Evidence sprint done", "0", "W1-W4", "G1"),
    ("M2 Pipeline correctness", "1", "W5-W10", "post-G1 placeholder"),
    ("M3 Continuous batching scales", "2", "W9-W13", "G2 with M4"),
    ("M4 MoE expert runtime parity", "2.5", "W11-W17", "G2"),
    ("M5 Heterogeneous frontier serve", "3", "W16-W24", "G3"),
    ("M6 Resilience / elasticity", "4", "W23-W27", "G4"),
    ("M7 Productization / GA", "5", "W27+", "G5"),
]

_ROLES = [
    ("DIST", "High", "Planner, cost model, v0 target contract"),
    ("RT", "Medium", "Runtime format and substrate ADR"),
    ("KER", "Medium", "Apple expert-MLP probe and Apple role decision"),
    ("NET", "Low", "Networking/security draft"),
    ("SRE", "Low-Med", "Preflight workflow and diagnostics"),
    ("PM", "Medium", "Gate, RAID, procurement, staffing"),
    ("TL/SP", "Low", "Review and decide"),
]


def render_program_rebaseline_draft(
    *,
    kickoff_date: str | None = None,
    phase0_weeks: int = 4,
    ker_status: str = "unassigned",
    scope: str = "pending",
) -> dict[str, Any]:
    """Render the Phase-0 roadmap rebaseline and staffing-answer draft."""

    if ker_status not in KER_STATUS_VALUES:
        raise ValueError(f"ker_status must be one of {', '.join(KER_STATUS_VALUES)}")
    if scope not in SCOPE_VALUES:
        raise ValueError(f"scope must be one of {', '.join(SCOPE_VALUES)}")
    if phase0_weeks <= 0:
        raise ValueError("phase0_weeks must be positive")
    kickoff = _parse_date(kickoff_date) if kickoff_date else None
    warnings = _warnings(ker_status=ker_status, scope=scope, kickoff=kickoff)
    markdown = _render_markdown(
        kickoff=kickoff,
        phase0_weeks=phase0_weeks,
        ker_status=ker_status,
        scope=scope,
        warnings=warnings,
    )
    return {
        "markdown": markdown,
        "ok": True,
        "warnings": warnings,
        "kickoff_date": kickoff.isoformat() if kickoff else None,
        "phase0_weeks": phase0_weeks,
        "ker_status": ker_status,
        "scope": scope,
        "staffing_answer": _staffing_answer(ker_status, scope),
    }


def _parse_date(value: str) -> date:
    try:
        return datetime.strptime(value, "%Y-%m-%d").date()
    except ValueError as exc:
        raise ValueError("kickoff_date must use YYYY-MM-DD") from exc


def _warnings(*, ker_status: str, scope: str, kickoff: date | None) -> list[str]:
    warnings: list[str] = []
    if kickoff is None:
        warnings.append("kickoff date is unset; absolute dates are not calculated")
    if ker_status == "unassigned":
        warnings.append("KER staffing answer remains open")
    if ker_status == "unavailable" and scope != "narrowed":
        warnings.append("KER unavailable: Sponsor should narrow Apple role or staff KER before G1")
    if scope == "pending":
        warnings.append("Sponsor scope decision is pending")
    return warnings


def _staffing_answer(ker_status: str, scope: str) -> str:
    if ker_status in {"in-team", "hireable", "contractable"}:
        return "KER staffing path exists; keep Apple expert-worker probe on the Phase-0 board."
    if ker_status == "unavailable" and scope == "narrowed":
        return "KER unavailable and scope narrowed; bias Apple to capacity-only for v0."
    if ker_status == "unavailable":
        return "KER unavailable; G1 must choose NARROW, ITERATE to staff, or KILL."
    return "KER staffing unresolved; G1 cannot silently proceed."


